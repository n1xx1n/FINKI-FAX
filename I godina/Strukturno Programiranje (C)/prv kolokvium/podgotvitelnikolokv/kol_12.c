/*
Од тастатура се читаат непознат број на позитивни цели броеви (со максимум 5 цифри) се додека не се внесе нешто различно од број. За секој број треба да се најде позицијата (од десно на лево) 
на првото појавување на најголемата цифра во составот на бројот (цифрата на единици се наоѓа на позиција 0). Потоа, да се испечати статистика за позициите на најдените цифри со максимална вредност 
во следниот формат:
[позиција]: [вкупно броеви чија максимална цифра се наоѓа на таа позиција]
*/
#include <stdio.h>

int main(){
    int a,cnt0=0,cnt1=0,cnt2=0,cnt3=0,cnt4=0;
    while(scanf("%d", &a)){
        int max=a%10,tmp=a,maxi=0;
        for(int i=0;i<5;i++){
            if(tmp%10>max){
                max=tmp%10;
                maxi=i;
            }
            tmp/=10;
        }
        switch(maxi){
            case 0:
                cnt0++;
                break;
            case 1:
                cnt1++;
                break;
            case 2:
                cnt2++;
                break;
            case 3:
                cnt3++;
                break;
            case 4:
                cnt4++;
                break;
        }
    }
    printf("0: %d\n1: %d\n2: %d\n3: %d\n4: %d\n", cnt0, cnt1, cnt2, cnt3, cnt4);
    return 0;
}
