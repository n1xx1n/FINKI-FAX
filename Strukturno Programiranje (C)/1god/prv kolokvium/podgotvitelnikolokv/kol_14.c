/*
Од тастатура се читаат непознат број позитивни цели броеви се додека не се внесе нешто различно од број. За секој број треба да се пресмета збирот на цифрата со максимална вредност од претходно 
внесениот број  и сумата на цифрите на тековниот број (за првиот внесен број се пресметува само сумата на неговите цифри). За секој внесен број резултатот треба да се испечати во следниот формат:
[број]: [сумата на цифрите на бројот + максималната цифра од претходниот број]
342: 9   //3+4+2
345: 16 //3+4+5+4
123: 11 //1+2+3+5
456: 18 //4+5+6+3
*/
#include <stdio.h>

int main(){
    int n;
    while(scanf("%d", &n)){
        int tmp=n,sumDigits=0,k=0,flag,max,maxprev;
        max=maxprev;
        maxprev=0;
        while(tmp>0){
            if(tmp%10>maxprev){
                maxprev=tmp%10;
            }
            sumDigits+=tmp%10;
            tmp/=10;
        }
        if (flag==0){
            printf("%d: %d\n", n, sumDigits+max);
            continue;
        }
        if(k==0){
            printf("%d: %d\n", n, sumDigits);
            flag=0;
        }
    }
    return 0;
}
