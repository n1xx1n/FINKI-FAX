#include <stdio.h>
#include <ctype.h>
#include <string.h>
#include <math.h>

float absolute (float x){
    if(x<0){
        return x*(-1.0);
    }
    return x;
}

int main(){
    int n,m,i,j;
    int a[100][100];
    float arr[100],niza[100];
    scanf("%d %d", &n, &m);
    for(i=0;i<n;i++){
        for(j=0;j<m;j++){
            scanf("%d", &a[i][j]);
        }
    }
    for(i=0;i<n;i++){
        for(j=0;j<m;j++){
            arr[i]=arr[i]+a[i][j];
        }
        arr[i]= (float) arr[i]/m;
        float max=0;
        niza[i]=a[i][0];
        for(j=0;j<m;j++){
            float r=absolute(a[i][j]-arr[i]);
            if(r>max){
                max=r;
                niza[i]=a[i][j];
            }
        }
    }
    for(i=0;i<n;i++){
        printf("%d ", (int) niza[i]);
    }
    return 0;
}