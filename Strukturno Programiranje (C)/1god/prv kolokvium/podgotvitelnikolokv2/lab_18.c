/*
Од тастатура се внесуваат димензиите на една матрица (m,n <= 100), а потоа и елементите од матрицата. Да се трансформира матрицата така што средниот елемент во секоја
редица ќе се замени со разликата (по апсолутна вредност) на сумата на елементите во првата половина од редицата и сумата на елементите во втората половина на редицата.
Ако матрицата има парен број колони, се менува вредноста на средните два елемента. Средниот/те елемент/и влегува/ат во сумите (при непарен број на колони, средниот елемент
                                                                                                                                                                                                                                      влегува во двете суми!). Да се испечати на екран променетата матрица.
*/
#include <stdio.h>
#include <ctype.h>
#include <string.h>
#include <stdlib.h>
#include <math.h>

int main(){
    int n,m,i,j;
    float a[100][100],b[100][100];
    scanf("%d", &n);
    for(i=0;i<n;i++){
        for(j=0;j<n;j++){
            scanf("%f", &a[i][j]);
        }
    }
    float sumglav=0,sumspor=0;
    for(i=0;i<n;i++){
        for(j=0;j<n;j++){
            if(i>j){
                sumglav+=a[i][j];
            }
            if(i+j>n-1){
                sumspor+=a[i][j];
            }
        }
    }
    for(i=0;i<n;i++){
        for(j=0;j<n;j++){
            if(i==j && i+j==n-1){
                b[i][j]=sumglav+sumspor;
            }else if(i+j==n-1){
                b[i][j]=sumspor;
            }else if(i==j){
                b[i][j]=sumglav;
            }else{
                b[i][j]=0;
            }
        }
    }
    for(i=0;i<n;i++){
        for(j=0;j<n;j++){
            printf("%.1f ", b[i][j]);
        }
        printf("\n");
    }
    return 0;
}
